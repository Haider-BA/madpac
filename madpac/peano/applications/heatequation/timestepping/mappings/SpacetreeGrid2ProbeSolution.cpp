void peano::applications::heatequation::timestepping::mappings::SpacetreeGrid2ProbeSolution::touchVertexFirstTime(
  peano::applications::heatequation::timestepping::SpacetreeGridSingleStepVertex&               fineGridVertex,
  const tarch::la::Vector<DIMENSIONS,double>&                                                   fineGridX,
  const tarch::la::Vector<DIMENSIONS,double>&                                                   fineGridH,
  peano::applications::heatequation::timestepping::SpacetreeGridSingleStepVertex const * const  coarseGridVertices,
  const peano::kernel::gridinterface::VertexEnumerator&                                         coarseGridVerticesEnumerator,
  const peano::applications::heatequation::timestepping::SpacetreeGridCell&                     coarseGridCell,
  const tarch::la::Vector<DIMENSIONS,int>&                                                      fineGridPositionOfVertex
) {
  logTraceInWith6Arguments( "touchVertexFirstTime(...)", fineGridVertex, fineGridX, fineGridH, coarseGridVerticesEnumerator.toString(), coarseGridCell, fineGridPositionOfVertex );

  bool isProbePoint = fineGridVertex.getRefinementControl()==SpacetreeGridSingleStepVertex::Records::Unrefined;
  for (int d=0; d<DIMENSIONS; d++) {
    isProbePoint &= (fineGridX(d)==1.0/3.0) || (fineGridX(d)==2.0/3.0);
  }

  if (isProbePoint) {
    _probeNorm += fineGridVertex.getTemperature() * fineGridVertex.getTemperature();
    logInfo( "touchVertexFirstTime(...)", "probe at " << fineGridX << ": " << fineGridVertex.getTemperature() );
  }

  logTraceOutWith1Argument( "touchVertexFirstTime(...)", fineGridVertex );
}

